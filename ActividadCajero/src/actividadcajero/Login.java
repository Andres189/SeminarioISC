    private void btnEntrar1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEntrar1MouseClicked

        if(txtNIP.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this,"No se ingreso NIP\nNo se reducen los intentos maximos","ERROR",JOptionPane.ERROR_MESSAGE);
           txtNIP.setText(""); 
        }  
        else if(!txtNIP.getText().trim().equals(NIP)){
            Contador--;
            JOptionPane.showMessageDialog(this,"NIP incorrecto\n"+Contador+" intentos restantes","ERROR",JOptionPane.ERROR_MESSAGE);
            txtNIP.setText("");
        }else if(txtNIP.getText().trim().equals(NIP)){
            txtNIP.setText("");
            JOptionPane.showMessageDialog(this,"Bienvenido","Inicio de sesion",1);
            Contador=3;
            MenuCajero menu=new MenuCajero();
            menu.setVisible(true);
            this.setVisible(false);

        }
        if(Contador==0){
            JOptionPane.showMessageDialog(this,"Intentos maximos superados","Bloqueo de sesion",1);
            System.exit(0);
        }
        
    }//GEN-LAST:event_btnEntrar1MouseClicked

    private void txtNIPKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNIPKeyTyped
        int key = evt.getKeyChar();
        
        boolean numeros = key >48 && key <=57;
        if(txtNIP.getText().trim().length()>3||!numeros){
            evt.consume();
        }
        
        
    }//GEN-LAST:event_txtNIPKeyTyped

